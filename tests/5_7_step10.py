"""
Реализуйте функцию aggregation(func, sequence), которая принимает:

func — функцию, принимающую два аргумента;

sequence — список элементов.

Функция aggregation должна последовательно применять func к элементам списка
и возвращать список промежуточных результатов.
Сначала функция применяется к первым двум элементам списка.
Затем полученный результат используется вместе со следующим элементом и так далее.

Пример
В качестве func возьмем функцию

def get_add(x, y):
    return x + y


Коллекцией у нас будет следующий список

numbers = [5, 2, 4, 3, 5]


Применяя func к первым элементам коллекции 5 и 2, получим сумму 7. Это первое наше агрегированное значение.
Далее берем уже накопленное значение 7 и следующий необработанный элемент 4, суммируем и получаем новую агрегацию 11.

Тогда шаги выполнения будут выглядеть так:

get_add(5, 2) → 7
get_add(7, 4) → 11
get_add(11, 3) → 14
get_add(14, 5) → 19
"""


def aggregation(func, sequence):
    # Если последовательность пустая, возвращаем пустой список
    if not sequence:
        return []

    # Если в последовательности только один элемент, возвращаем его в виде списка
    if len(sequence) == 1:
        return [sequence[0]]

    # Инициализируем список для хранения промежуточных результатов
    result = []

    # Начальное значение — первый элемент последовательности
    current_value = sequence[0]

    # Проходим по оставшимся элементам последовательности (начиная со второго)
    for element in sequence[1:]:
        # Применяем функцию func к текущему накопленному значению и следующему элементу
        current_value = func(current_value, element)
        # Добавляем полученный результат в список промежуточных результатов
        result.append(current_value)

    return result
